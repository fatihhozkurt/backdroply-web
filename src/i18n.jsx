const LanguageContext = createContext(null);

export function LanguageProvider({ children }) {
  const [lang, setLang] = useState(
    () => localStorage.getItem("backdroply_lang") || localStorage.getItem("clipcut_lang") || "tr"
  );
  const t = useMemo(() => dictionaries[lang] || dictionaries.tr, [lang]);
  const value = useMemo(
    () => ({
      lang,
      setLang: (next) => {
        localStorage.setItem("backdroply_lang", next);
        localStorage.setItem("clipcut_lang", next);
        setLang(next);
      },
      t
    }),
    [lang, t]
  );
  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(LanguageContext);
  if (!ctx) {
    throw new Error("useI18n must be used within LanguageProvider");
  }
  return ctx;
}
